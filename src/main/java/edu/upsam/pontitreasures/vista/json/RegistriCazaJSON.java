package edu.upsam.pontitreasures.vista.json;

public class RegistriCazaJSON {
	
	private String cazaId;
	
	private String jugadorIdentificador;

	/**
	 * @return the cazaId
	 */
	public String getCazaId() {
		return cazaId;
	}

	/**
	 * @param cazaId the cazaId to set
	 */
	public void setCazaId(String cazaId) {
		this.cazaId = cazaId;
	}

	/**
	 * @return the jugadorIdentificador
	 */
	public String getJugadorIdentificador() {
		return jugadorIdentificador;
	}

	/**
	 * @param jugadorIdentificador the jugadorIdentificador to set
	 */
	public void setJugadorIdentificador(String jugadorIdentificador) {
		this.jugadorIdentificador = jugadorIdentificador;
	}
	
	

}
